package com.auroratms.officials;

/**
 * Represents referee ranks from highest (IR) to lowest (CR)
 * IR 	International Referee
 * NR 	National Referee
 * RR 	Regional Referee
 * CR 	Certified Referee
 */
public enum RefereeRank {
    CertifiedReferee,
    RegionalReferee,
    NationalReferee,
    InternationalReferee
}
