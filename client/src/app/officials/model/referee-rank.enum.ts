/**
 * Represents referee ranks from highest (IR) to lowest (CR)
 * IR 	International Referee
 * NR 	National Referee
 * RR 	Regional Referee
 * CR 	Certified Referee
 */
export enum RefereeRank {
  CertifiedReferee= 'CertifiedReferee',
  RegionalReferee= 'RegionalReferee',
  NationalReferee= 'NationalReferee',
  InternationalReferee= 'InternationalReferee'
}
